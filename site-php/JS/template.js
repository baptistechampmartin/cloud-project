var images = [];
var img;
var currentIndex;
var body = document.getElementsByTagName('body')[0];
var columns = document.getElementsByClassName('column');
var darkBg = document.getElementById('dark-bg');
var i = 0;

for (var i = 0 ; i < photosUrl.length ; i++) {

	img = document.createElement('img');
	img.id = photosUrl[i];
	img.className = 'photo';
	img.onload = function() {
		images.push(this);
	};
	img.src = photosUrl[i];
	img.onclick = function(e) {
		darkBg.style.display = 'flex';
		body.style.overflow = 'hidden';
		document.getElementById('photo-zoom').src = e.target.src;
		currentIndex = photosUrl.indexOf(e.target.id);
	};
}

var waitPhotosLoading = setInterval(function() {
	if (images.length === photosUrl.length) {
		for (var i = 0; i < images.length; i++) {
			if (
			columns[0].clientHeight === 0 ||
			(columns[0].clientHeight <= columns[1].clientHeight &&
			columns[0].clientHeight <= columns[2].clientHeight)
			) {
				columns[0].appendChild(images[i]);
			} else if (
				columns[1].clientHeight === 0 ||
				(columns[1].clientHeight <= columns[0].clientHeight &&
				columns[1].clientHeight <= columns[2].clientHeight)
				) {
				columns[1].appendChild(images[i]);
			} else {
				columns[2].appendChild(images[i]);
			}
		}
		clearInterval(waitPhotosLoading);
	}
}, 50);

function changeToNextPhoto(toNextOne) {
	if (toNextOne) {
		currentIndex++;
		if (currentIndex === photosUrl.length) {
			currentIndex = 0;
		}
	} else {
		currentIndex--;
		if (currentIndex < 0) {
			currentIndex = photosUrl.length - 1;
		}
	}
	document.getElementById('photo-zoom').src = photosUrl[currentIndex];
}

function clickOutside(e) {
	if (e.target === darkBg) {
		darkBg.style.display = 'none';
		body.style.overflow = 'auto';
	}
}
